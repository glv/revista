  live_reload: [
    patterns: [
      ~r{priv/static/.*(js|css|png|jpeg|jpg|gif|svg)$},
      ~r{priv/gettext/.*(po)$},
      ~r{lib/admin/views/.*(ex)$},
      ~r{lib/admin/templates/.*(eex)$}
    ]
  ]
